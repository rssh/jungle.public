
package ua.gradsoft.jungle.gwt.util.client;

/**
 *Interface, which incapsulate dimensions for resolution.
 * @author rssh
 */
public interface Resolution {

    /**
     *@return width of 'X' in pixels (for normal font).
     **/
    public int getXWidth();

    /**
     *@return width of 'W' in pixels (for normal font).
     **/
    public int getWWidth();

    /**
     *@return height of 'X' in pixels (for normal font).
     **/
    public int getXHeight();


}
